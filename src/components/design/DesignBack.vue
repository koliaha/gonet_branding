  computed: {
    colorList() {
      return color_back_mini
    },
    colorMain() {
      return color_back
    }
  },
  methods: {
    roRgb(str) {
      let aRgbHex = str.match(/.{1,2}/g);
      let aRgb = [
        parseInt(aRgbHex[0], 16),
        parseInt(aRgbHex[1], 16),
        parseInt(aRgbHex[2], 16)
      ];
      return aRgb.join(' ')
    },

  },
